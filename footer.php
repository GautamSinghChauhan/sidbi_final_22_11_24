            $("#documentfile_name").remove();
            $("#documentfile_modulename").remove();
            $('#frmuserdownloads').append("<input type='hidden' name='documentfile' id='documentfile_name' value='" + $(this).attr("data-documentfile") + "' />");
            $('#frmuserdownloads').append("<input type='hidden' name='documentmodulename' id='documentfile_modulename' value='" + $(this).attr("data-modulename") + "' />");
