                        <h2>MSME FORMALIZATION PROJECT: UDYAM ASSIST PLATFORM</h2>
                    </div>
                    <div class="address-number-website">
                        <ul class="download-complete-list">
                            <li class="download-complete-lists">India has a large number of Informal Micro Enterprises (IMEs) not having the required documentary evidence or ability to self-register on Udyam portal. In this background, Ministry of MSME has authorised SIDBI to develop the 'Udyam Assist Platform' (UAP) for enabling the assist registration process of IMEs with the help of Designated Agencies (DAs). DAs comprise regulated entities like banks, NBFCs, etc. along with government departments. MoMSME has also authorised SIDBI to collect data of IMEs (including information of Aadhar) from DAs to register IMEs and issue them Udyam Registration Number (URN) and Udyam Assist Certificate (UAC). To address this, Ministry of MSME and SIDBI jointly started working on the MSME Formalization project, wherein in all informal enterprises shall be registered on the Udyam Assist Platform (UAP) and subsequently shall be linked to various facilities such as access to markets, access to credit, subsidy etc. An Assist methodology with the help of Designated Agencies like Banks, MFIs, NBFCs, etc. to register the informal enterprises on UAP has been adopted. The UAP was implemented and formally inaugurated by the Hon’ble Minister of MSMEs, GoI on January 11, 2023. Over 130 DAs are enrolled on UAP. Till date, over 90 lakh IMEs are registered on UAP with the number expected to grow in the coming months.</li>
