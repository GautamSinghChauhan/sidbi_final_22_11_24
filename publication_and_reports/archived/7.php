        var registrationData = $('#frmuserdownloads').serializeArray();
        registrationData.push({
            name: 'download_id',
            value: download_id
        });
        var para = download_id.split('_');
        if (para[3] == 'Structural') {
            //var redirect = redurl+'en/Articles/download/'+para[1]+'/'+para[2]+'/'+para[3];
            var redirect = 'index';
